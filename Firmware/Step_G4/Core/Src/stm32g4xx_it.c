/**
  * @brief This function handles TIM4 global interrupt.
  */
void TIM4_IRQHandler(void)
{
  /* USER CODE BEGIN TIM4_IRQn 0 */
	static uint16_t count = 0;
  /* USER CODE END TIM4_IRQn 0 */
  HAL_TIM_IRQHandler(&htim4);
  /* USER CODE BEGIN TIM4_IRQn 1 */
	count++;				//计算速度/累计角度//设置目标角度/速度转角度/更新信息
	pid.Bias_Positoin-=cmd_speed;
	pid.Integral_bias_speed-=cmd_speed;
	if(pid.Tim_limit>0)
		pid.Tim_limit--;
	if(pid.L_position != 0)
	{
		if(abs(pid.L_position)>abs(pid.L_speed))
		{
			pid.L_position-=pid.L_speed;
		}
		else
		{
			pid.L_speed = pid.L_position;
			pid.L_position = 0;
		}
		pid.Bias_Positoin-=pid.L_speed;
		pid.Integral_bias_speed-=pid.L_speed;
	}
	if(count == 25)
	{
		pid.Bias_Positoin-=low_speed;
		pid.Integral_bias_speed-=low_speed;
		ADC_Updata();
		Inform_Updata(pid.R_speed*600000/16384.0,myADC.volt,myADC.temperature);
		count = 0;
	}
	if(Ce_flag!=0)//自动计算参数 but还没写
	{
		static uint16_t fft_count = 1024;
		fft_count--;
		
		if(fft_count!=0)
		{
			
		}
	}
  /* USER CODE END TIM4_IRQn 1 */
}

/**
  * @brief This function handles USART1 global interrupt / USART1 wake-up interrupt through EXTI line 25.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
	if(__HAL_UART_GET_FLAG(&huart1,UART_FLAG_IDLE) == SET)
	{
		__HAL_UART_CLEAR_IDLEFLAG(&huart1);
		HAL_UART_DMAStop(&huart1);
		uart1_rx_count = 16 - __HAL_DMA_GET_COUNTER(&hdma_usart1_rx);
		Manage_Command();
		HAL_UART_Receive_DMA(&huart1,uart1_rx_buffer,16);
		HAL_UART_Transmit_DMA(&huart1,(uint8_t *)updata_info+2,31);
	}
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

